# 1) 프로그래밍: 특정 목적을 이루기 위해 프로그램 언어를 사용해 구체적인 프로그램을 작성하는 과정
# 2) 인터프리터: 소스 코드를 한 라인씩 읽으며 기계어로 번역하는 프로그램
# 3) 컴파일러: 소스 코드 전체를 한 번에 번역하는 프로그램
# 4) 통합개발환경(IDE): 프로그래밍을 돕는 소프트웨어. 디버깅, 실행, 배포, 버전 관리 등 많은 기능을 제공한다.
# 5) 프롬프트: 사용자가 명령어를 입력할 수 있는 터미널
# 6) 라이브러리: 다른 사람들이 프로그래밍의 생산성을 높이기 위해 모듈화해 기능 구현한 코드의 집합
# 7) 문자열: 문자의 집합
# 8) 표현식: 평가의 대상, 즉 (파이썬의 경우) 인터프리터가 읽어 실행해야 하는 코드
